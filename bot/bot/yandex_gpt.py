def _call_yandex_gpt(system_prompt: str, user_prompt: str, temperature: float = 0.5, max_tokens: int = 2000) -> str:
    """Делает запрос к YandexGPT, возвращает текст ответа."""
    headers = {
        "Authorization": f"Api-Key {YC_API_KEY}",
        "Content-Type": "application/json",
    }
    payload = {
        "modelUri": f"gpt://{YC_FOLDER_ID}/{YC_MODEL}",
        "completionOptions": {
            "temperature": temperature,
            "maxTokens": str(max_tokens),
        },
        "messages": [
            {"role": "system", "text": system_prompt},
            {"role": "user", "text": user_prompt},
        ],
    }
    response = requests.post(YC_API_URL, headers=headers, json=payload, timeout=60)
    response.raise_for_status()
    data = response.json()
    return data["result"]["alternatives"][0]["message"]["text"]


def _extract_json(text: str) -> Any:
    """Извлекает JSON из ответа модели, отбрасывая markdown-обёртки если есть."""
    text = text.strip()
    text = re.sub(r"^```(?:json)?\s*", "", text)
    text = re.sub(r"\s*```$", "", text)
    return json.loads(text)


def _fallback(role: str) -> dict:
    """Возвращает пустую структуру если модель не справилась."""
    return {
        "overview": f"Не получилось собрать впечатление {role}а. Попробуйте ещё раз через минуту.",
        "strengths": [],
        "weaknesses": [],
        "recommendations": [],
    }


def analyze_as_recruiter(profile_text: str) -> dict:
    """Анализирует профиль как рекрутер."""
    user_msg = f"Профиль LinkedIn:\n\n{profile_text}"
    try:
        raw = _call_yandex_gpt(RECRUITER_SYSTEM_PROMPT, user_msg, temperature=0.5)
        return _extract_json(raw)
    except Exception:
        logger.exception("Рекрутер: ошибка анализа")
        return _fallback("рекрутер")


def analyze_as_colleague(profile_text: str) -> dict:
    """Анализирует профиль как коллега."""
    user_msg = f"Профиль LinkedIn:\n\n{profile_text}"
    try:
        raw = _call_yandex_gpt(COLLEAGUE_SYSTEM_PROMPT, user_msg, temperature=0.5)
        return _extract_json(raw)
    except Exception:
        logger.exception("Коллега: ошибка анализа")
        return _fallback("коллег")


def analyze_profile(profile_text: str) -> dict:
    """Анализирует профиль с двух точек зрения, возвращает структуру для Mini App."""
    return {
        "recruiter": analyze_as_recruiter(profile_text),
        "colleague": analyze_as_colleague(profile_text),
    }
